export const floorOneDecimal = (num: number) => {
  return Math.floor(num * 10) / 10;
};

export const floorTwoDecimal = (num: number) => {
  return Math.floor(num * 100) / 100;
};

export const floorThreeDecimal = (num: number) => {
  return Math.floor(num * 1000) / 1000;
};
export const floorFourDecimal = (num: number) => {
  return Math.floor(num * 10000) / 10000;
};

export const floorFiveDecimal = (num: number) => {
  return Math.floor(num * 100000) / 100000;
};

export const floorSixDecimal = (num: number) => {
  return Math.floor(num * 1000000) / 1000000;
};
